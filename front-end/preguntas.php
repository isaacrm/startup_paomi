						<p align="left"><b>✔¿Cuál es el concepto de Sheer Boxes?</b> 
Nuestro concepto es hacerte disfrutar la experiencia de 'descubrimiento', es decir poder mostrarte todos esos productos que jamás comprarías por tu parte y hacerte probar las últimas tendencias. El método de compra es innovador ya que te suscribes a través de nuestra plataforma digital, llenas un perfil que nos permite conocer tus gustos y en base a eso preparamos las combinaciones de productos ideales para ti. ¡Está de moda experimentar nuevas marcas y dejarte sorprender mes a mes!</p>
 
<p align="left"><b>✔¿Cuándo y adónde recibo mi caja una vez pagada?</b>
Tu caja la entregamos a domicilio según la dirección que indicas en tu perfil al suscribirte. Ésta la recibirás durante la última semana de cada mes.
Nota: si te suscribiste después del día 20 del mes entonces recibirás la caja del mes siguiente.</p>
 
<p align="left"><b>✔¿Porqué es seguro pagar tu caja con tarjeta? </b> 
Es muy seguro tu pago en tarjeta vía teléfono ya que te atendemos directamente y no a través de terceros. Además tu pago se hace al instante y no guardamos ningún registro del numero de tu tarjeta y te hacemos llegar el comprobante rápidamente. 
Si eres de los que prefiere NO pagar en tarjeta entonces tenemos otras opciones de pago que seguramente se adaptan mejor a tus expectativas.</p> 
 
<p align="left"><b>✔¿Si me suscribo al plan 1 Mes me cobran todos los meses?</b>  
No. Precisamente ese plan está hecho para todos aquellos que son nuevos en esta experiencia y quieren probar con una sola caja. Al elegir ese plan tu pagas tu caja los meses que quieras. </p>
 
<p align="left"><b>✔¿Puedo recibir mi caja si me encuentro fuera de El Salvador?</b> 
Por le momento solo distribuimos en esos países pero estamos trabajando para poder llegar a más países este 2015. </p>
 
<p align="left"><b>✔¿Es normal si mi caja no era igual a la de mi amiga? </b> 
Claro que si. Recuerda que tu perfil nos permite hacer las combinaciones personalizadas según tus gustos y necesidades, eso explica la diferencia de tu caja con la de tu amiga. También recuerda que somos muy flexibles y tu opinión es muy importante, por eso si te gustó algún producto de tu amiga en particular no dudes en escribirnos y lo tomaremos en cuenta para tu siguiente caja. </p>
 
